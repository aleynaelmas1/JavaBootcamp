package miniProjeSesliSessizHarfler;

public class miniProjeSesliSessizHarfler {

	public static void main(String[] args) {
		// Kalın Sesli ve İnce Sesli Harfler
		char harf='I';
		
		switch(harf) {
		case 'A':
		case 'I':
		case 'O':
		case 'U':
			System.out.println("Kalın sesli");
			break;
			default:
				System.out.println("İnce sesli");
		
		
		
		}
		}

	}


